public class CalculateSum {
    public static void main(String[] args) {
        /*
         * Find the sum from 1 to 100 using any loop statements
         * and print out the sum
         */
        int sum = 0;
        for (int i = 1; i < 101; i++) {
            sum = sum + i;
        }
        System.out.println(sum);

    }
}
